// Antes da OO (Orientada a objeto) tinhamos o paradigma Procedural e antes do Procedural era o código não estruturado (go-to)

// Procedural  // Você tem funções que manipulam dados
processamento(valor1, valor2, valor3) // Processamento manipula dados e retorna um valor



// Orientada a Objeto // Você tem dados e dentro dos dados tem funções
objeto = {  // Objetos que tem valores (atributos) e funções (comportamentos) dentro do objeto
    valor1, 
    valor2,
    valor3,
    processamento(){
        // ...
    }
}

objeto.processamento()  // O foco passou a ser o objeto // E pode invocar os processamentos apartir do dado, ou seja, a função processamento percente a objeto 

// Objeto funciona como uma cápsula, que agrupa comportamentos e atributos.

// Principios importantes: 
// 1. Abstração = Traduzir itens da vida real de forma adequada para determinado software ex: sistema detran não precisa de saber tudo do funcionamento, diferente de um sistema de oficina
// 2. Encapsulamento = Ter os detalhes de implementação escondidos e mostrar ao usuário uma interface simples ex: carro, você só usa o volante e os meios mas não precisa se preocupar com o funcionamento por dentro do motor, etc (outro exemplo: trocar a forma de passar a marcha do carro você tem que se acustumar(ex de troca de interface), mas se muda apenas o motor ou algo interno mas a 'interface a msm', você não precisa se preocupar )
// 3. Herança (prototype) = (Tipo 'é um') Herdar funções de um objeto, ex: tem atributos, comportamentos, caracteristicas em um objeto e você pode usa-las dentro de outro objeto herdando eles
// 4. Polimorfismo = 'multiplas formas' ex: celular (conceito genérico) porém, posso apontar para vários celular, por exemplo: Celular -> galaxy s5, porém posso apontar também para outro objeto, por ex: Celular -> galaxy s98, ou objeto Embarcacao (objeto genecio) navio pode ser por exemplo Embarcacao -> Iate, Embarcacao -> Navio







 